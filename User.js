class User {
  constructor(firstName, lastName, age) {
    this.firstName = firstName;
    this.lastName = lastName;
    this.age = age;
  }

  get name() {
    return this.name;
  }

  set name(name) {
    this.name = name;
  }

  get lastName() {
    return this.lastName;
  }

  set lastName(lastName) {
    this.lastName = lastName;
  }

  get age() {
    return this.age;
  }

  set age(age) {
    this.age = age;
  }
}
